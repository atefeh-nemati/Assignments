A = float(input("shoae ostovane ra vared konid: "))
B = float(input("ertefae ostovane ra vared konid: "))
pi = 3.14

Masahatjanebi = 2 * pi * A * B
print ("masahate janebi: ",Masahatjanebi)

Hajm = pi * (A**2) * B
print ("hajm: ",Hajm)

MasahateKol = 2 * (pi * A * B) + 2 * (pi * (A**2))
print ("masahate kol: ",MasahateKol)