Shoa = float(input("shoae ostovane ra vared konid: "))
Ertefa = float(input("ertefae ostovane ra vared konid: "))
pi = 3.14

Masahatjanebi = 2 * pi * Shoa * Ertefa
print ("masahate janebi: ",Masahatjanebi)

Hajm = pi * (Shoa**2) * Ertefa
print ("hajm: ",Hajm)

MasahateKol = 2 * (pi * Shoa * Ertefa) + 2 * (pi * (Shoa**2))
print ("masahate kol: ",MasahateKol)